# Visual type → data role mapping for dynamic projection building
# ---------------------------------------------------------------------------
_VISUAL_ROLES: dict[str, dict[str, str]] = {
    # visual_type -> {dimension_role, measure_role, series_role}
    "card":                    {"measure": "Fields"},
    "clusteredBarChart":       {"dimension": "Category", "measure": "Y", "series": "Series"},
    "clusteredColumnChart":    {"dimension": "Category", "measure": "Y", "series": "Series"},
    "columnChart":             {"dimension": "Category", "measure": "Y", "series": "Series"},
    "lineChart":               {"dimension": "Category", "measure": "Y", "series": "Series"},
    "areaChart":               {"dimension": "Category", "measure": "Y", "series": "Series"},
    "pieChart":                {"dimension": "Category", "measure": "Y"},
    "donutChart":              {"dimension": "Category", "measure": "Y"},
    "treemap":                 {"dimension": "Group", "measure": "Values"},
    "scatterChart":            {"measure": "Y", "dimension": "Details"},
    "map":                     {"dimension": "Category", "measure": "Size"},
    "filledMap":               {"dimension": "Category", "measure": "Values"},
    "pivotTable":              {"dimension": "Rows", "measure": "Values", "series": "Columns"},
    "table":                   {"dimension": "Values", "measure": "Values"},
    "lineClusteredColumnComboChart": {"dimension": "Category", "measure": "Y", "measure2": "Y2"},
}


def _build_dynamic_projections(
    worksheet: WorksheetIR, visual_type: str
) -> dict[str, list[dict[str, str]]]:
    """Build projections dynamically from worksheet field references."""
    roles = _VISUAL_ROLES.get(visual_type, {"dimension": "Category", "measure": "Y"})
    projections: dict[str, list[dict[str, str]]] = {}

    dim_role = roles.get("dimension", "Category")
    measure_role = roles.get("measure", "Y")
    series_role = roles.get("series")

    # Cols typically map to the category axis, rows to measures (in Tableau convention)
    # But for cards: text encoding → Fields
    if visual_type == "card":
        # Use text encoding or first aggregated field
        enc = worksheet.encodings
        if enc.text and enc.text.table_name:
            measure_name = _make_measure_name(enc.text)
            projections.setdefault("Fields", []).append(
                {"queryRef": f"{enc.text.table_name}.{measure_name}"}
            )
        elif worksheet.rows:
            ref = worksheet.rows[0]
            table = ref.table_name or "Data"
            measure_name = _make_measure_name(ref)
            projections.setdefault("Fields", []).append(
                {"queryRef": f"{table}.{measure_name}"}
            )
        return projections

    # Dimensions from cols (category axis)
    for ref in worksheet.cols:
        table = ref.table_name or "Data"
        if ref.derivation in ("Month",):
            col_name = f"{ref.column_name}_Month"
        elif ref.derivation in ("Day",):
            col_name = f"{ref.column_name}_Day"
        elif ref.derivation and ref.derivation not in ("None", "Year", "Quarter"):
            # Aggregated column on axis → treat as measure
            measure_name = _make_measure_name(ref)
            projections.setdefault(measure_role, []).append(
                {"queryRef": f"{table}.{measure_name}"}
            )
            continue
        else:
            col_name = ref.column_name
        projections.setdefault(dim_role, []).append(
            {"queryRef": f"{table}.{col_name}"}
        )

    # Measures from rows
    for ref in worksheet.rows:
        table = ref.table_name or "Data"
        if ref.derivation and ref.derivation not in ("None", "Month", "Day", "Year", "Quarter"):
            measure_name = _make_measure_name(ref)
            projections.setdefault(measure_role, []).append(
                {"queryRef": f"{table}.{measure_name}"}
            )
        elif ref.derivation in ("Month",):
            col_name = f"{ref.column_name}_Month"
            projections.setdefault(dim_role, []).append(
                {"queryRef": f"{table}.{col_name}"}
            )
        elif ref.derivation in ("Day",):
            col_name = f"{ref.column_name}_Day"
            projections.setdefault(dim_role, []).append(
                {"queryRef": f"{table}.{col_name}"}
            )
        else:
            projections.setdefault(dim_role, []).append(
                {"queryRef": f"{table}.{ref.column_name}"}
            )

    # Series/legend from color encoding
    enc = worksheet.encodings
    if series_role and enc.color and enc.color.table_name:
        if not enc.color.derivation or enc.color.derivation == "None":
            projections.setdefault(series_role, []).append(
                {"queryRef": f"{enc.color.table_name}.{enc.color.column_name}"}
            )

    # Size encoding for maps/scatter
    if enc.size and enc.size.table_name and enc.size.derivation and enc.size.derivation != "None":
        size_role = "Size" if visual_type in ("map", "scatterChart") else measure_role
        measure_name = _make_measure_name(enc.size)
        projections.setdefault(size_role, []).append(
            {"queryRef": f"{enc.size.table_name}.{measure_name}"}
        )

    # LOD/detail encoding for maps
    if enc.lod and enc.lod.table_name:
        if visual_type in ("map", "filledMap"):
            projections.setdefault(dim_role, []).append(
                {"queryRef": f"{enc.lod.table_name}.{enc.lod.column_name}"}
            )

    return projections


def _make_measure_name(ref: FieldReference) -> str:
    """Create a DAX measure name from a field reference (matches translation output)."""
    if not ref.derivation or ref.derivation == "None":
        return ref.column_name
    return f"{ref.derivation} of {ref.column_name}"


def map_worksheet_to_visual(worksheet: WorksheetIR, index: int) -> VisualSpec:
    """Map a Tableau worksheet to a Power BI visual specification."""
    ws_name = worksheet.name

    # Determine visual type — try overrides first, then infer dynamically
    visual_type = WORKSHEET_VISUAL_OVERRIDES.get(ws_name)
    if not visual_type:
        visual_type = _infer_visual_type(worksheet)

    # Determine projections — use hardcoded if available, else build dynamically
    projections = WORKSHEET_PROJECTIONS.get(ws_name, {})
    if not projections:
        projections = _build_dynamic_projections(worksheet, visual_type)

    # Calculate layout position
    x, y, width, height = _calculate_layout(ws_name, index)

    # Clean name for use as identifier
    safe_name = "Vis_" + ws_name.replace(" ", "_")

    return VisualSpec(
        name=safe_name,
        visual_type=visual_type,
        title=ws_name,
        x=x,
        y=y,
        width=width,
        height=height,
        projections=projections,
    )


def _infer_visual_type(worksheet: WorksheetIR) -> str:
    """Infer Power BI visual type from worksheet characteristics."""
    mark = worksheet.mark_type

    # Card detection: no rows/cols, only text encoding
    if not worksheet.rows and not worksheet.cols:
        if worksheet.encodings.text:
            return "card"

    # Map detection
    if worksheet.encodings.geometry:
        return "filledMap"

    # Use mark type map
    return MARK_TYPE_MAP.get(mark, "clusteredColumnChart")


def _calculate_layout(ws_name: str, index: int) -> tuple[int, int, int, int]:
    """Calculate visual position and size on the Power BI canvas."""
    # y-offsets account for the title textbox (height=50, padding=8) at top
    _title_offset = 58
    layout_map: dict[str, tuple[int, int, int, int]] = {
        "Total Revenue": (8, 8 + _title_offset, 265, 119),
        "Total Orders": (273, 8 + _title_offset, 312, 119),
        "Average Customers": (585, 8 + _title_offset, 310, 119),
        "Max Payment": (895, 8 + _title_offset, 317, 119),
        "Month wise Order": (8, 135 + _title_offset, 602, 320),
        "Category wise Revenue": (618, 135 + _title_offset, 602, 320),
        "Payment Volume": (8, 463 + _title_offset, 1204, 200),
        "Order Volume": (8, 671 + _title_offset, 1204, 240),
        "Productwise Quantity": (8, 919 + _title_offset, 582, 270),
        "Monthwise Average Quantity": (598, 919 + _title_offset, 622, 270),
        "Month wise shipment status": (8, 1197 + _title_offset, 1204, 300),
        "Ratings": (8, 1505 + _title_offset, 1204, 310),
        "Statewise Amount": (8, 1823 + _title_offset, 602, 310),
        "Statewise Revenue": (618, 1823 + _title_offset, 602, 310),
        "Carrier Amount": (8, 2141 + _title_offset, 602, 280),
        "Product wise Price": (618, 2141 + _title_offset, 602, 280),
    }

    if ws_name in layout_map:
        return layout_map[ws_name]

    # Fallback grid layout
    col = index % 2
    row = index // 2
    x = 8 + col * 620
    y = 8 + row * 300
    return (x, y, 600, 280)


def map_all_worksheets(worksheets: list[WorksheetIR]) -> list[VisualSpec]:
    """Map all worksheets to Power BI visual specifications."""
    visuals = []
    for i, ws in enumerate(worksheets):
        spec = map_worksheet_to_visual(ws, i)
        visuals.append(spec)
    logger.info("Mapped %d worksheets to Power BI visuals", len(visuals))
    return visuals
