@dataclass
class VisualSpec:
    """Specification for a Power BI visual."""
    name: str
    visual_type: str
    title: str
    x: int = 0
    y: int = 0
    width: int = 300
    height: int = 250
    projections: dict[str, list[dict[str, str]]] = field(default_factory=dict)


# Worksheet → Power BI visual type (derived from actual Tableau mark types in Shopping (1).twb)
#
# TWB analysis:
#   Total Revenue / Total Orders / Average Customers / Max Payment
#       → Automatic mark, no rows/cols, text encoding only  → card
#   Category wise Revenue
#       → Automatic mark, rows=category, cols=SUM(price)    → clusteredBarChart (horizontal)
#   Carrier Amount
#       → Pie mark, color=carrier, wedge=SUM(amount)         → pieChart
#   Month wise Order
#       → Automatic mark, cols=MONTH(order_date), rows=COUNT(order_id)  → lineChart
#   Monthwise Average Quantity
#       → Dual-axis (Bar pane + Automatic), cols=MONTH(order_date),
#         rows=AVG(quantity)+SUM(total_price)               → lineClusteredColumnComboChart
#   Month wise shipment status
#       → Area mark, color=shipment_status, cols=MONTH(shipment_date),
#         rows=SUM(total_price)                             → areaChart
#   Payment Volume
#       → Automatic mark, cols=payment_method, rows=COUNTD(payment_id),
#         color=transaction_status                         → columnChart
#   Order Volume
#       → Automatic mark, cols=MONTH(order_date), rows=DAY(order_date),
#         color=COUNT(order_id)  (heatmap)                  → matrix
#   Productwise Quantity
#       → Square mark, rows=product_name, color/size=SUM(quantity)  → treemap
#   Ratings
#       → Automatic mark (histogram), cols=Rating(bin), rows=COUNT(rating) → clusteredColumnChart
#   Statewise Amount
#       → Automatic mark on map, lod=Clean_state, size=SUM(amount) → map
#   Statewise Revenue
#       → Automatic mark on map, lod=Clean_state, color=SUM(amount) → filledMap
#   Product wise Price
#       → Shape mark, rows=SUM(price_at_purchase), lod=product_name → scatterChart
